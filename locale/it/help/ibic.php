<H2 align=center>INTRODUZIONE</H2>
<P>&nbsp;</P>
Il sistema IBIC si basa su 2.600 codici di soggetti, raggruppati gerarchicamente in 18 categorie (arte, di lingua, letteratura e studi letterari, narrativa e argomenti correlati, Giurisprudenza, Medicina ...). Allo stesso modo, propone  900 codici denominati 'qualificatori'  per indicare, se lo si desidera, la localizzazione geografica, le lingue dell'opera, i periodi storici, i fini didattici e l'età di interesse. 
<H2>Funzionamento</H2>
<P>&nbsp;</P>

<P>Il sistema è stato sviluppato nel Regno Unito da BIC (Book Industry Communication - http://www.bic.org.uk) per servire una richiesta comune per il commercio dei libri. Nel 1998 ha pubblicato la prima versione dello standard, alla cui elaborazione hanno partecipato rappresentanti di spicco del settore. L'ultima versione (v 2.1), che è stato il punto di riferimento per il suo adattamento spagnolo è stata pubblicata nel dicembre 2010. Nel mese di ottobre 2011, la classificazione BIC viene rinominata in IBIC per raccogliere adattamenti di sistema per i mercati differenti come Italia, Spagna, Germania, Norvegia, Svezia, Portogallo, ecc </ P>
<P> In Spagna, la scelta dei soggetti di classificazione IBIC per il mercato del libro è il risultato di un accordo tra i rappresentanti di tutto il settore commerciale (editori, distributori, librerie e ISBN Agency), coordinato dalla Federazione delle Gilde degli editori spagnoli per mezzo del DILVE. </ P>
<P> Al momento si sta svolgendo una analisi più dettagliata e una collaborazione scientifica e tecnica con alcuni dei più importanti editori e librerie nonché in coordinamento con il comitato internazionale IBIC. </ P >
