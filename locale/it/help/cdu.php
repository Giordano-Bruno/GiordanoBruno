<H2 align=center>INTRODUZIONE</H2>
<P>&nbsp;</P>

La Classificazione decimale universale (nota anche con la denominazione inglese Universal Decimal Classification, con la denominazione tedesca Universelle Dezimalklassifikation e con le sigle CDU, UDC e UDK)
è uno schema di classificazione sviluppato dai bibliografi belgi Paul Otlet ed Henri La Fontaine alla fine del secolo XIX. Essa è basata sulla Classificazione decimale Dewey, ma è molto più potente. 
Essa si serve di segni ausiliari per indicare vari aspetti particolari di un soggetto e delle relazioni fra soggetti. In tal modo contiene un elemento significativo faccettato o analitico-sintetico che la rende adatta soprattutto nelle
biblioteche specialistiche. La CDU nel corso degli anni è stata modificata ed estesa per adattarla con la crescita delle pubblicazioni che si riscontra in tutte le discipline ed è tuttora sottoposta a revisioni continue per tener conto
di tutti i nuovi sviluppi.
La CDU consente di classificare documenti di ogni genere: innanzi tutto documenti della letteratura, cioè documenti scritti, ma anche su altri media come registrazioni su pellicola, video, registrazioni sonore,
illustrazioni, mappe e artefatti (realia) come pezzi da museo.
<H2>Funzionamento</H2>
<P>&nbsp;</P>
La CDU per gli identificatori dei soggetti si serve della numerali arabi e di notazioni del sistema decimale. Ogni identificatore va considerato come scrittura decimale di un numero frazionale (con la omissione di un eventuale 
punto decimale iniziale) che determina l'ordinamento degli insiemi di sigle. Per facilità di lettura di solito un identificatore CDU viene completato con un punto ogni tre cifre. In tal modo dopo l'identificatore 61 "Scienze mediche" 
vengono le suddivisioni da 611 a 619; sotto 611 "Anatomia" vengono le suddivisioni da 611.1 a 611.9; sotto 611.1 vengono tutte le sue suddivisioni che precedono 611.2 e così via; dopo 619 viene l'identificatore 62. Un vantaggio di 
questo sistema sta nella sua illimitata raffinabilità (ed estensibilità): per introdurre nuove suddivisioni non si devono modificare gli identificatori preesistenti.

