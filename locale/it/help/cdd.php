<H2 align=center>INTRODUZIONE</H2>
<P>&nbsp;</P>
La Classificazione decimale Dewey (anche DDC, acronimo di Dewey Decimal Classification) è uno schema di classificazione bibliografica per argomenti organizzati gerarchicamente, sviluppato nel 1876 da Melvil Dewey (1851-1931);
dalla prima redazione è stato modificato e accresciuto, con ventitre revisioni principali, l'ultima delle quali nel 2011. È molto diffusa in biblioteche delle più diverse tipologie, dove consente di determinare in modo univoco 
la distribuzione dei libri nei diversi settori; laddove non occorrano notazioni molto particolareggiate si utilizza in genere in edizione ridotta.
<H2>Funzionamento</H2>
<P>&nbsp;</P>
La chiave di volta del sistema è l'impiego dei decimali che permette di tradurre esclusivamente in cifre le informazioni. Vi è anche la possibilità di realizzare una classificazione su più fronti (faceted classification), 
combinando elementi da parti diverse della struttura e costruendo un codice numerico che rappresenta nell'insieme il contenuto (a sua volta ottenuto combinando l'argomento con gli elementi geografici e temporali) e la
forma del testo stesso. Ogni cifra è portatrice di un significato completo in sé (indica una categoria-insieme), che viene maggiormente precisato dall'aggiunta delle cifre successive (sottocategorie-sottoinsiemi). 
Non sono previste meno di tre cifre: se si intende dare indicazione solo dell'insieme maggiore, si utilizza la prima seguita da due zeri. Ad esempio, in biblioteca un libro che riporta il numero 513 è un testo scientifico (5) 
che si occupa di matematica (1) e in particolare di aritmetica (3). Il libro potrebbe portare anche solo il numero 500, ma in questo caso sapremmo soltanto che è un testo scientifico e niente di più. Il tipo di classificazione,
ad esclusione delle opere generali e della narrativa, è basato principalmente sull'argomento, con estensioni per le correlazioni, luoghi, tempo, tipo di opera, dando così luogo a classificazioni di lunghezza indeterminata 
(fermo restando che il primo gruppo deve essere completo), con un punto ogni tre cifre. Per esempio 330 indica l'economia + 94 per l'Europa = 330.94 Economia europea; 973 per gli Stati Uniti + 005 che contraddistingue i 
periodici = 973.005, pubblicazioni periodiche in genere relative agli Stati Uniti; le classificazioni devono essere lette e ordinate come numeri: 050, 220, 330.973, 331 e così via.
